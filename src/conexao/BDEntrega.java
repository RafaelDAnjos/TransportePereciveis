    public synchronized ArrayList selectTable() {
        ArrayList listEntregas = new ArrayList();        
        Connection c = null;
        Statement stmt = null;
        try {
            Class.forName("org.postgresql.Driver");
            c = DriverManager.getConnection("jdbc:postgresql://localhost:5432/testeJava", "postgres", "serra");
            //c.setAutoCommit(false);
            System.out.println("Banco de dados aberto com sucesso!!");
            stmt = c.createStatement();
            
            ResultSet rs = stmt.executeQuery("SELECT * FROM entrega ;");
            while (rs.next()) {
                Entrega entrega = new Entrega();
                entrega.setHorarioSaidaFrigorifico(rs.getString("horarioSaidaFrigorifico"));
                entrega.setHorarioChegadaFrigorifico(rs.getString("horarioChegadaFrigorifico"));
                entrega.setHorarioEntregaPedido(rs.getString("horarioEntregaPedido"));
                listEntregas.add(entrega);
            }
            rs.close();
            stmt.close();
            c.close();
        } catch (Exception e) {
            System.err.println(e.getClass().getName() + ": " + e.getMessage());            
        }
        System.out.println("Operação realizada com sucesso!!");
        return listEntregas;
    }
    
