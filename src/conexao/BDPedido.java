    public synchronized ArrayList selectTable() {
        ArrayList listPedidos = new ArrayList();        
        Connection c = null;
        Statement stmt = null;
        try {
            Class.forName("org.postgresql.Driver");
            c = DriverManager.getConnection("jdbc:postgresql://localhost:5432/testeJava", "postgres", "serra");
            //c.setAutoCommit(false);
            System.out.println("Banco de dados aberto com sucesso!!");
            stmt = c.createStatement();
            
            ResultSet rs = stmt.executeQuery("SELECT * FROM funcionario ;");
            while (rs.next()) {
                Pedido pedido = new Pedido();
                pedido.setNumeroBandas(rs.getInt("numeroBandas"));
                pedido.setDataDeEntrega(rs.getString("dataDeEntrega"));              
                pedido.setId(rs.getInt("id"));
                listPedidos.add(pedido);
            }
            rs.close();
            stmt.close();
            c.close();
        } catch (Exception e) {
            System.err.println(e.getClass().getName() + ": " + e.getMessage());            
        }
        System.out.println("Operação realizada com sucesso!!");
        return listPedidos;
    }
    
